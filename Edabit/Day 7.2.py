#Return a String as an Integer
#Create a function that takes a string and returns it as an integer.
#string_int("6") ➞ 6
#string_int("1000") ➞ 1000
#string_int("12") ➞ 12
#All numbers will be whole numbers.
def string_int(txt):
    return int(txt)
print(string_int("6"))
print(string_int("1000") )
print(string_int("12"))
